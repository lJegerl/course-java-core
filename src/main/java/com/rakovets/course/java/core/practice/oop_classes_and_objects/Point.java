package com.rakovets.course.java.core.practice.oop_classes_and_objects;

public class Point {
    private double x;
    private double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getDistande(Point point) {
        double distance;
        distance = Math.pow(point.getX() - getX(), 2) + Math.pow(point.getY() - getY(), 2);
        return Math.pow(distance, 0.5);
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }
}
